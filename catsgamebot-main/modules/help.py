    """–û–±—Ä–∞–±–æ—Ç–∫–∞ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏ –ø–æ —Å–ø—Ä–∞–≤–∫–µ"""
    section = callback.data.split(":")[1]
    
    if section == "basics":
        await show_basics(callback)
    elif section == "fishing":
        await show_fishing(callback)
    elif section == "upgrades":
        await show_upgrades(callback)
    elif section == "bosses":
        await show_bosses(callback)
    elif section == "guilds":
        await show_guilds(callback)
    elif section == "swamp":
        await show_swamp(callback)
    elif section == "cases":
        await show_cases(callback)
    elif section == "donate":
        await show_donate(callback)
    elif section == "commands":
        await show_commands(callback)
    elif section == "fish_list":
        await show_fish_list(callback)
    elif section == "fish_rarity":
        rarity = callback.data.split(":")[2]
        await show_fish_by_rarity(callback, rarity)
    else:
        await callback.answer("–†–∞–∑–¥–µ–ª –≤ —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–µ")

