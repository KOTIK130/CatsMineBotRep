async def show_fish_by_rarity(callback: CallbackQuery, rarity: str):
    """–ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç —Ä—ã–± –ø–æ —Ä–µ–¥–∫–æ—Å—Ç–∏"""
    if rarity == "all":
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –≤—Å–µ—Ö —Ä—ã–±
        text = "üêü <b>–í—Å–µ –≤–∏–¥—ã —Ä—ã–±</b>\n\n"
        
        # –ì—Ä—É–ø–ø–∏—Ä—É–µ–º –ø–æ —Ä–µ–¥–∫–æ—Å—Ç–∏
        by_rarity = {}
        for fish_type, fish_data in FISH_TYPES.items():
            fish_rarity = fish_data["rarity"]
            if fish_rarity not in by_rarity:
                by_rarity[fish_rarity] = []
            by_rarity[fish_rarity].append((fish_type, fish_data))
        
        # –ü–æ—Ä—è–¥–æ–∫ —Ä–µ–¥–∫–æ—Å—Ç–∏
        rarity_order = ["common", "uncommon", "rare", "epic", "legendary", "mythic"]
        
        for rarity_key in rarity_order:
            if rarity_key in by_rarity:
                rarity_color = RARITY_COLORS[rarity_key]
                rarity_name = RARITY_NAMES[rarity_key]
                text += f"{rarity_color} <b>{rarity_name}:</b>\n"
                
                for fish_type, fish_data in by_rarity[rarity_key]:
                    text += (
                        f"  {fish_data['emoji']} {fish_data['name']}\n"
                        f"     üí∞ {fish_data['price']}$ | üéØ {fish_data['chance']}% | üé£ –£—Ä.{fish_data['min_level']}+\n"
                    )
                text += "\n"
    else:
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ä—ã–± –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–π —Ä–µ–¥–∫–æ—Å—Ç–∏
        rarity_color = RARITY_COLORS.get(rarity, "‚ö™")
        rarity_name = RARITY_NAMES.get(rarity, "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è")
        
        text = f"{rarity_color} <b>{rarity_name} —Ä—ã–±–∞</b>\n\n"
        
        fish_found = False
        for fish_type, fish_data in FISH_TYPES.items():
            if fish_data["rarity"] == rarity:
                fish_found = True
                text += (
                    f"{fish_data['emoji']} <b>{fish_data['name']}</b>\n"
                    f"üí∞ –¶–µ–Ω–∞: <b>{fish_data['price']}$</b>\n"
                    f"üéØ –®–∞–Ω—Å: <b>{fish_data['chance']}%</b>\n"
                    f"üé£ –ú–∏–Ω. —É—Ä–æ–≤–µ–Ω—å: <b>{fish_data['min_level']}</b>\n\n"
                )
        
        if not fish_found:
            text += "–í —ç—Ç–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –ø–æ–∫–∞ –Ω–µ—Ç —Ä—ã–±—ã."
    
    keyboard = InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text="üîô –ö —Å–ø–∏—Å–∫—É —Ä—ã–±", callback_data="help:fish_list")]
    ])
    
    await callback.message.edit_text(text, reply_markup=keyboard, parse_mode="HTML")

